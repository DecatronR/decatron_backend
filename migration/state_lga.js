async function migrateStatesAndLGAs() {
  try {
    console.log("Starting migration of Nigerian States and LGAs...");

    // Clear existing data (uncomment if you want to start fresh)
    // await State.deleteMany({});
    // await LGA.deleteMany({});
    // console.log("Cleared existing data");

    let stateCount = 0;
    let lgaCount = 0;

    for (const stateData of nigeriaData) {
      // Create state
      const stateSlug = createSlug(stateData.state);

      // Check if state already exists
      let existingState = await State.findOne({ slug: stateSlug });

      if (!existingState) {
        const newState = new State({
          slug: stateSlug,
          state: stateData.state,
          createdAt: new Date(),
        });

        existingState = await newState.save();
        stateCount++;
        console.log(`Created state: ${stateData.state}`);
      } else {
        console.log(`State already exists: ${stateData.state}`);
      }

      // Create LGAs for this state
      for (const lgaName of stateData.lgas) {
        const lgaSlug = createSlug(lgaName);

        // Check if LGA already exists
        const existingLGA = await LGA.findOne({ slug: lgaSlug });

        if (!existingLGA) {
          const newLGA = new LGA({
            stateId: existingState._id.toString(),
            slug: lgaSlug,
            lga: lgaName,
            createdAt: new Date(),
          });

          await newLGA.save();
          lgaCount++;
        }
      }

      console.log(
        `Processed ${stateData.lgas.length} LGAs for ${stateData.state}`
      );
    }

    console.log(`\nMigration completed successfully!`);
    console.log(`States created: ${stateCount}`);
    console.log(`LGAs created: ${lgaCount}`);
    console.log(`Total states: ${nigeriaData.length}`);
    console.log(
      `Total LGAs: ${nigeriaData.reduce(
        (sum, state) => sum + state.lgas.length,
        0
      )}`
    );
  } catch (error) {
    console.error("Migration failed:", error);
    throw error;
  }
}

