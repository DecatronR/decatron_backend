// Function to get LGA count by state
async function getLGACountByState() {
  const pipeline = [
    {
      $lookup: {
        from: "states",
        localField: "stateId",
        foreignField: "_id",
        as: "state",
      },
    },
    {
      $unwind: "$state",
    },
    {
      $group: {
        _id: "$state.state",
        count: { $sum: 1 },
      },
    },
    {
      $sort: { _id: 1 },
    },
  ];

  return await LGA.aggregate(pipeline);
}

